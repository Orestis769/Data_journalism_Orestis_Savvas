#Ζητά από τον χρήστη να γράψει ένα κείμενο.
text = input("Παρακαλώ, γράψτε ένα κείμενο: ")
#Ζητά μια λέξη.
word = input("Παρακαλώ, γράψτε μια λέξη: ")
#Μετρά πόσες φορές εμφανίζεται η λέξη στο κείμενο.
count = text.count(word)
#Εμφανίζει το αποτέλεσμα.
print(f"Η λέξη '{word}' εμφανίζεται {count} φορές στο κείμενο")